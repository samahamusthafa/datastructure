class Queue{
    constructor(size){
        this.array=Array(size)
        this.front=-1
        this.rear=-1
    }
    enqueue(value){
        if(this.rear==-1){
            this.front=0
            this.rear=0
            this.array[0]=value
            return
        }
        this.rear++
        this.array[this.rear]=value
    }
    dequeue(){
        if(this.front==-1){
            return
        }else if(this.front==this.rear){
            this.front=-1
            this.rear=-1
            return
        }
    }
}