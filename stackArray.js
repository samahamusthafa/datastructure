class Stack{
    constructor(size){
        this.array=Array(size)
        this.top=-1
    }
    push(element){
        this.top++
        this.array[this.top]=element
    }
    pop(){
        if(this.top==-1){
            return null
        }
        this.top--
    }
    peek(){
        return this.array[this.top]
    }
    size(){
        return this.top+1
    }
    print(){
        if(this.top==-1){
            return null
        }else{
            for(let i=0;i<=this.top;i++){
                console.log(this.array[i])
            }
        }
    }
}

